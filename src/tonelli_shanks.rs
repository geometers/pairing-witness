#[derive(Clone, Copy)]
pub(crate) struct TS {
    // 27th_root
    pub(crate) w: Fq12,
}

impl TS {
    fn get_pow_3_order(a: Fq12) -> u32 {
        let mut a = a;
        let mut t = 0;
        while a != Fq12::one() {
            t += 1;
            a = a.pow(&[3, 0, 0, 0]);
        }

        return t;
    }
    // panics if can't find a cube root after 100 iterations or a doesn't have inverse
    pub fn cube_root(&self, a: Fq12) -> Fq12 {
        let a_inv = a.inverse().unwrap();
        let mut x = exp(a, &EXP_0);
        let mut t = Self::get_pow_3_order(exp(a, &K));

        // println!("T0 IS: {}", t);

        let mut cnt = 0;

        while t != 0 {
            let exp = 3u64.pow(POW_3 - t - 1);
            x = x * self.w.pow(&[exp, 0, 0, 0]);
            t = Self::get_pow_3_order(x.pow(&[3, 0, 0, 0]) * a_inv);

            cnt += 1;
            if cnt == 100 {
                panic!("Can't find cube root after 100 iterations");
            }
        }

        x
    }
}

#[cfg(test)]
mod ts_tests {
    use ark_std::{
        rand::{rngs::StdRng, SeedableRng},
        UniformRand,
    };

    use super::*;
    use crate::utils::sample_27th_root_of_unity;

    #[test]
    fn test_cube_root() {
        let rng = &mut StdRng::seed_from_u64(12313132u64);
        let w = sample_27th_root_of_unity(rng);

        let ts = TS { w };

        for _ in 0..10 {
            let x = Fq12::rand(rng);
            let a = x.pow(&[3, 0, 0, 0]);

            let root = ts.cube_root(a);
            assert_eq!(root.pow(&[3, 0, 0, 0]), a);
        }
    }
}
